           <h3>PROBLEMAS AUDITIVOS</h3>

          <h1>PERDA AUDITIVA</h1>

        <p>A audição é o primeiro dos cinco sentidos desenvolvidos pelos seres humanos e desempenha importante função para as pessoas. Ela amplia nossa percepção do mundo, através da captação dos sons do ambiente e sons da fala modificando emoções e comportamentos no decorrer de nossas vidas.</p>

        <p>A perda auditiva ocorre quando há qualquer problema que acometa algum componente sistema auditivo, sejam na condução do som, na transmissão do mesmo para o nervo auditivo ou em ambas as condições, assim, dependendo da região comprometida, a perda auditiva pode configurar-se como dos tipos condutiva, neurossensorial ou mista, respectivamente. Com a perda auditiva, a captação e a transmissão do som para as vias auditivas podem ficar comprometidas em diferentes graus, podendo ser leve, moderada, severa ou profunda. Quanto maior o grau, maior dificuldade terá para ouvir e entender a conversação.</p>

        <p>A identificação do tipo e grau da perda auditiva é realizada mediante o exame de audiometria e os resultados obtidos com esse exame serão ilustrados no gráfico, chamado audiograma. O seu audiograma é uma foto de sua audição e apresenta quantitativamente sua capacidade auditiva, indicando quando há audição normal, ou no caso de perda auditiva, o tipo e o grau do comprometimento. O diagnóstico deve ser feito por um profissional fonoaudiólogo ou otorrinolaringologista, que de acordo com os resultados, irá lhes orientar quanto à solução mais apropriada.</p>

        <p>Quanto mais cedo for identificada e tratada a perda auditiva, menos consequências negativas surgirão, como dificuldades em entender a conversação, falar ao telefone, ouvir a televisão e consequente isolamento social, depressão, entre outros problemas cognitivos. Na maioria dos casos, o uso de aparelhos de amplificação sonora é a solução mais apropriada, por não ser tão invasivo e oferecer diferentes opções de adaptação, a depender da perda auditiva, das queixas e demandas auditivas da pessoa. A indicação deve ser realizada pelo médico otorrinolaringologista e a seleção, junto ao processo de adaptação, deve ser realizada e  acompanhada por um fonoaudiólogo.</p>

        <h1>TRANSTORNO DO PROCESSAMENTO AUDITIVO</h1>
         <p>O processamento auditivo central se refere a capacidade do indivíduo de interpretar aquilo que escuta, pois, só ter limiares auditivos normais não garante uma boa compreensão. Precisamos para isso, reconhecer, discriminar e entender aquilo que escutamos.</p>

 <p>Estas habilidades se referem ao Processamento Auditivo Central e são de extrema importância para o aprendizado e desenvolvimento da língua oral e escrita, bem
como outros aspectos cognitivos e psicossociais. Pessoas que ouvem, mas não entendem, mesmo tendo audição normal ou sendo usuário de aparelhos auditivos
e têm dificuldades para localização da fonte sonora, problemas de aprendizagem, atenção, concentração e memória, podem apresentar alterações nas habilidades
do Processamento Auditivo Central.</p>

 <p>O diagnostico é realizado a partir de uma investigação medica e fonoaudiológica, mediante a avaliação do Processamento Auditivo Central, que consiste na  aplicação de uma bateria de testes para cada habilidade auditiva, entre outros exames complementares.</p>

 <p>O tratamento se da mediante atividades de treinamento auditivo e o tempo do processo terapêutico varia de pessoa para pessoa, dependendo das habilidades
auditivas que se encontram alteradas e de outros distúrbios associados.</p>

<h1>ZUMBIDO</h1>
<p>O zumbido é a sensação de estar ouvindo um som, sem que haja uma fonte sonora relacionada. Estima-se que no Brasil o zumbido acomete até 28 milhões
de pessoas. Segundo a  American Public Health Agency, ele pode considerado o terceiro sintoma que mais causa incômodo perdendo apenas para dor e tontura intensas e intratáveis.  É um sintoma que pode estar relacionado a diversas causas, como problemas metabólicos, hormonais, efeitos de medicamentos, problemas psicológicos, problemas cardiovasculares, problemas musculares e também problemas auditivos. O zumbido interfere negativamente na qualidade de vida, gerando problemas de concentração, sono e no equilíbrio emocional. </p>

<p>O quadro do zumbido e as condições do paciente devem ser cuidadosamente investigados pelo médico Otorrinolaringologista, que poderá também realizar encaminhamentos para outros profissionais, como o fonoaudiólogo, dentista, fisioterapeuta, entre outros. Após esta investigação, o médico irá prescrever o melhor tratamento para cada caso.</p>
