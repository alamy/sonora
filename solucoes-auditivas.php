<button class="accordion">ADAPTAÇÃO DE APARELHOS DE AMPLIFICAÇÃO SONORA</button>
<div class="panel">
  <p>Temos profissionais capacitados e com vasta experiência no processo de seleção e adaptação de Aparelhos de Amplificação Sonora Individual (AASI). O processo de seleção é realizado em uma avaliação, na qual o fonoaudiólogo audiologista irá considerar as demandas auditivas do paciente, bem como sua perda auditiva para, partir disso, selecionar o modelo e a tecnologia do aparelho auditivo que mais de adequa as necessidades do paciente.</p>

  <p>Após a aquisição do aparelho auditivo, o paciente prosseguirá com o processo de adaptação, mediante as consultas de acompanhamento junto ao fonoaudiólogo. Estas consultas são de extrema importância, pois, além de oferecer orientações acerca do uso e realizar ajustes necessários no aparelho auditivo, o paciente terá sessões de treinamento auditivo, que consiste em promover atividades voltadas à estimulação das habilidades auditivas que podem estar prejudicadas em função da perda de audição. O processo de adaptação envolve um trabalho conjunto do paciente, fonoaudiólogo e familiares, visando à promoção de uma adaptação eficiente e consequentemente, o reestabelecimento da qualidade de vida. </p>
</div>

<button class="accordion">ADAPTAÇÃO PEDIÁTRICA</button>
<div class="panel">
  <p>A criança que possui perda auditiva apresenta demandas maiores e mais específicas para o uso dos Aparelhos de Amplificação Sonora Individual. Estas demandas envolvem, além da questão estética (tamanho dos aparelhos e cores), a forma de processamento dos sons, a qual deve proporcionar um ambiente perceptivo auditivo amplo e favorável ao aprendizado. Ainda, a adaptação pediátrica deve atender as demandas do ambiente escolar e familiar, de forma a oferecer acessibilidade, segurança e comodidade no uso dos aparelhos auditivos nestes diferentes contextos. Outro aspecto importante envolve as medições de ganho e verificação das próteses auditivas, as quais subsidiam o fonoaudiólogo para um ajuste mais fino e adequado dos aparelhos auditivos na criança. A Sonora possui uma linha específica de aparelhos auditivos voltados para crianças a linha pediátrica, levando em consideração as demandas específicas deste grupo.</p>
</div>

<button class="accordion">TREINAMENTO AUDITIVO</button>
<div class="panel">
  <p>As habilidades auditivas podem se apresentar alteradas em pessoas com audição normal e de todas as idades, entretanto, estudos mostram que a partir de 65 anos o processamento auditivo já começa a presentar alterações, caso haja perdas auditivas essas alterações apresentam-se ainda mais evidenciadas.</p>
<p>A perda auditiva acarreta consequências significativas para as habilidades do Processamento Auditivo de compreensão e interpretação dos sons, uma vez que, o percurso do som, da via auditiva até o cérebro (córtex auditivo) encontra-se prejudicado. O grau das dificuldades depende de variados fatores, entre eles o tempo de privação auditiva pela falta de estimulação sonora adequada, bem como a causa de perda auditiva, a idade e outras condições.</p>
<p>Usuários de aparelhos auditivos podem sentir dificuldades em se acostumar com o novo mundo sonoro. Diante disto, mesmo após adaptação de aparelhos auditivos, os usuários podem apresentar muitas queixas, principalmente em situações de fala com mais de um interlocutor ou em ambientes ruidosos, evidenciando assim prejuízos das habilidades necessárias para uma boa compreensão nestas situações.</p>
<p>O treinamento auditivo voltado para usuários de aparelhos auditivos (AASI) e/ou Implante Coclear visa aperfeiçoar os benefícios dos aparelhos, melhorando as habilidades auditivas, bem como proporcionando um maior desempenho na discriminação e compreensão de fala. A terapia é fundamentada na capacidade de neuroplasticidade, que é o processo de modificação das funções cerebrais e reestabelecimento de habilidades mediante estímulos e atividades.
</p>
</div>


<button class="accordion">TERAPIA DE ZUMBIDO</button>
<div class="panel">
  <p>A Sonora disponibiliza de tratamento para zumbido, baseada no <i>Tinnitus Activies Terapy</i> (TAT). Este tratamento tem como fundamento principal a ressiginificação da percepção e reação do paciente em relação ao seu zumbido e envolve as estratégias de Aconselhamento e Enriquecimento Sonoro. Pode-se utilizar Aparelhos Auditivos com a função do <b>Gerador de Som</b> (<i>Tinnitus</i>) como instrumento auxiliar de tratamento, o qual emite sons ou ruídos que tem como função favorecer o processo de habituação e modificar a percepção que o paciente tem do zumbido. O tratamento é voltado para pessoas que possuem zumbido, com ou sem perda auditiva, ou seja, o mesmo aparelho auditivo pode amplificar os sons do ambiente, oferecendo a audibilidade que o paciente precisa. O tempo e a configuração do processo terapêutico vão depender do grau de incomodo que o paciente apresenta com o zumbido, bem como as repercussões negativas trazidas por este.</p>
</div>


<button class="accordion">VERIFICAÇÃO DAS PRÓTESES AUDITIVAS (GANHO INSERÇÃO E MAPEAMENTO VISÍVEL DE FALA)</button>
<div class="panel">
  <p>As adaptações dos aparelhos auditivos são realizadas a partir dos resultados obtidos no exame audiométrico. Os demais ajustes e personalizações irão depender da percepção e conforto do paciente, usando o aparelho auditivo no dia-a-dia.</p>
 <p>Além disso, para garantir uma adaptação mais segura e precisa, dispomos de equipamentos de verificação eletroacústica, dentre eles, o <b>Ganho de Inserção</b> e o <b>Mapeamento Visível de Fala</b>, os quais possibilitam analisar a amplificação fornecida pelo aparelho auditivo dentro do ouvido do paciente. Com os testes de verificação, é possível medir o desempenho dos aparelhos auditivos, de forma objetiva, dando subsídios para um ajuste mais apropriado da amplificação sonora.</p>
<p>A partir dos resultados obtidos com estes testes, a fonoaudióloga realizará os  ajustes necessários em cada frequência, para tornar os sons mais audíveis e confortáveis, principalmente os sons da fala.
</p>
</div>




      
      
